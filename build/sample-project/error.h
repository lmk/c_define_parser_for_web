
#ifndef __ERROR_H__
#define __ERROR_H__

enum DB_ERROR {
    DB_ERROR_NONE = 1,
    DB_ERROR_OPEN,
    DB_ERROR_READ,
    DB_ERROR_WRITE = 10
};

#define ERROR_INIT 0x0100
#define ERROR_MIDDLE 0x0200
#define ERROR_FINAL 0x0300

#define ERROR_SERVER_INIT_OPEN      (SERVER + ERROR_INIT    + DB_ERROR_OPEN)
#define ERROR_SERVER_INIT_OPEN      (SERVER + ERROR_INIT    + DB_ERROR_READ)
#define ERROR_SERVER_INIT_OPEN      (SERVER + ERROR_INIT    + DB_ERROR_WRITE)
#define ERROR_SERVER_MIDDLE_OPEN    (SERVER + ERROR_MIDDLE  + DB_ERROR_OPEN)
#define ERROR_SERVER_MIDDLE_OPEN    (SERVER + ERROR_MIDDLE  + DB_ERROR_READ)
#define ERROR_SERVER_MIDDLE_OPEN    (SERVER + ERROR_MIDDLE  + DB_ERROR_WRITE)
#define ERROR_SERVER_FINAL_OPEN     (SERVER + ERROR_FINAL   + DB_ERROR_OPEN)
#define ERROR_SERVER_FINAL_OPEN     (SERVER + ERROR_FINAL   + DB_ERROR_READ)
#define ERROR_SERVER_FINAL_OPEN     (SERVER + ERROR_FINAL   + DB_ERROR_WRITE)
#define ERROR_CLIENT_INIT_OPEN      (CLIENT + ERROR_INIT    + DB_ERROR_OPEN)
#define ERROR_CLIENT_INIT_OPEN      (CLIENT + ERROR_INIT    + DB_ERROR_READ)
#define ERROR_CLIENT_INIT_OPEN      (CLIENT + ERROR_INIT    + DB_ERROR_WRITE)
#define ERROR_CLIENT_MIDDLE_OPEN    (CLIENT + ERROR_MIDDLE  + DB_ERROR_OPEN)
#define ERROR_CLIENT_MIDDLE_OPEN    (CLIENT + ERROR_MIDDLE  + DB_ERROR_READ)
#define ERROR_CLIENT_MIDDLE_OPEN    (CLIENT + ERROR_MIDDLE  + DB_ERROR_WRITE)
#define ERROR_CLIENT_FINAL_OPEN     (CLIENT + ERROR_FINAL   + DB_ERROR_OPEN)
#define ERROR_CLIENT_FINAL_OPEN     (CLIENT + ERROR_FINAL   + DB_ERROR_READ)
#define ERROR_CLIENT_FINAL_OPEN     (CLIENT + ERROR_FINAL   + DB_ERROR_WRITE)

#endif
